    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTConsulta.getModel();
        
        ConsultaDAO cdao = new ConsultaDAO();
        
        for(Consulta c: cdao.read()){
            
            modelo.addRow(new Object[]{
                c.getId_consulta(),
                c.getNome_medico(),
                c.getNome_paciente(),
                c.getData_consulta(),
                c.getHorario(),
                c.getCpf_paciente()
            });
            
        }
        
    }
    

